// Language management
let currentLanguage = localStorage.getItem('language') || 'el';

function toggleLanguage() {
    currentLanguage = currentLanguage === 'el' ? 'en' : 'el';
    localStorage.setItem('language', currentLanguage);
    updatePageLanguage();
    updateLanguageButton();
}

function updateLanguageButton() {
    const langButton = document.querySelector('.lang-toggle');
    if (langButton) {
        langButton.innerHTML = currentLanguage === 'el' 
            ? '<i class="fas fa-globe"></i> EN' 
            : '<i class="fas fa-globe"></i> ΕΛ';
    }
}

function updatePageLanguage() {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage] && translations[currentLanguage][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[currentLanguage][key];
            } else {
                element.innerHTML = translations[currentLanguage][key];
            }
        }
    });
    
    // Update document title
    updateDocumentTitle();
    
    // Update dates
    updateDates();
}

function updateDocumentTitle() {
    const currentPath = window.location.pathname;
    let titleKey = '';
    
    if (currentPath.includes('about')) {
        titleKey = currentLanguage === 'el' ? 'Σπουδές & Εμπειρία - Δημήτριος Νάκος' : 'Education & Experience - Dimitrios Nakos';
    } else if (currentPath.includes('projects')) {
        titleKey = currentLanguage === 'el' ? 'Έργα - Δημήτριος Νάκος' : 'Projects - Dimitrios Nakos';
    } else if (currentPath.includes('contact')) {
        titleKey = currentLanguage === 'el' ? 'Επικοινωνία - Δημήτριος Νάκος' : 'Contact - Dimitrios Nakos';
    } else {
        titleKey = currentLanguage === 'el' ? 'Δημήτριος Νάκος - Portfolio' : 'Dimitrios Nakos - Portfolio';
    }
    
    document.title = titleKey;
}

function updateDates() {
    const dateElements = document.querySelectorAll('.timeline-date');
    dateElements.forEach(element => {
        const originalText = element.textContent;
        if (currentLanguage === 'en') {
            if (originalText.includes('Τώρα')) {
                element.textContent = originalText.replace('Τώρα', 'Present');
            }
            if (originalText.includes('Ιουλίου')) {
                element.textContent = originalText.replace('1 Ιουλίου 2024 - 3 Οκτωβρίου 2024', 'July 1, 2024 - October 3, 2024');
            }
        } else {
            if (originalText.includes('Present')) {
                element.textContent = originalText.replace('Present', 'Τώρα');
            }
            if (originalText.includes('July')) {
                element.textContent = originalText.replace('July 1, 2024 - October 3, 2024', '1 Ιουλίου 2024 - 3 Οκτωβρίου 2024');
            }
        }
    });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    updatePageLanguage();
    updateLanguageButton();
});
